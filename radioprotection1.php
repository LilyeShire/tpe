		<p class="radioprotection">Marie Curie aurait pu utiliser des équipements 
		de protection individuelle contre l’exposition externe d’irradiation par
		les rayons Gamma, à savoir le port de gants, lunettes, tablier plombés...
		qui complète les mesures de prévention en réduisant très fortement l’intensité
		du rayonnement. Des lunettes en verre plombés, des caches thyroïdes, des caches
		gonades et des gants plombés lorsque l’exposition des mains ne peut être évité 
		est également nécessaire et assure une meilleure protection. </p>
