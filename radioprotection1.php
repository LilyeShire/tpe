		<h1 class="radioprotection">I) Principes de la radioprotection </h1>
		<section>
			<p class="radioprotection debut">En 1910, Marie Curie (conjointement avec André Debierne) 
			isole le radium métallique à l’état pur, élément très radioactif. 
			Mais elle ne prend alors aucune protection avec ces éléments. 
			Son chef de travaux Fernand Holweck lui aurait construit une feuille 
			de plomb qu’elle n’a jamais porté. Sa propre protection n’était donc pas son 
			souci premier. En revanche des témoignages soulignent son souci des autres 
			à qui elle obligeait de rester dans le jardin 20min après avoir manipulé 
			de fortes doses radioactives. De plus, Claudius Regaud directeur du pavillon 
			Pasteur de l’institut du radium avait obligé le personnel du laboratoire Curie 
			à faire des analyses de sang régulières. Si celles-ci étaient « anormales » 
			les employés devaient prendre un long congé en campagne. </p>
			<figure>
				<img src="images/radioprotection/image1.jpg" alt="Marie Curie"/>
				<figcaption>Portrait de Marie Curie</figcaption>
			</figure>
			<p class="radioprotection">Nous allons déterminer des formes de protections 
			que Marie Curie aurait pu utiliser lors de ses manipulations. 
			La radioprotection est un ensemble de techniques et d’actions qui ont pour 
			objet, d'une part d'évaluer les atteintes portées à l'homme par 
			les rayonnements ionisants et d'autre part, de mettre en œuvre les moyens 
			permettant de l'en protéger.</br>
			La radioprotection s’appuie sur trois grands principes :
				<ul>
					<li>le temps, plus l'exposition est longue, plus la dose reçue sera forte </li>
					<li> la distance, plus la personne s'éloigne de la source radioactive, 
					moins elle reçoit de radioactivité</li>
					<li>l'écran, avec un écran, une personne peut recevoir moins de radioactivité 
					car il arrête le rayonnement radioactif.</li>
				</ul>
			</p>
			<p class="radioprotection">L’identification des caractéristiques de la source, l’élévation de 
			l’exposition et la définition des moyens de prévention sont en pratique 
			très étroitement imbriquées dans la démarche d’optimisation de la radioprotection
			qui vise à réduire « autant que raisonnablement réalisable » le niveau d’exposition
			des personnes. Une bonne connaissance des doses d’irradiations conduit à une bonne 
			radioprotection. Or actuellement nous n’avons aucuns moyens d’établir les doses 
			exactes d’irradiation de Marie Curie.<br/>
			Nous nous baserons donc sur des estimations.<br/>
			Dans le cas de Marie Curie nous allons tenter d’établir une liste de 
			radioprotections possibles en nous basant sur les connaissances scientifiques de 1910.
			</p>
			<h2 class="radioprotection">A) Etude d'un moyen de radioprotection : l'écran</h2>
			<p class="radioprotection">Il est impossible d’arrêter une source radioactive. En revanche,
			il est possible de s’en protéger. Dans le cas d’une exposition externe il 
			suffit de placer un écran entre soi et la source pour absorber les rayonnements. 
			</p>
			<figure>
				<img src="images/radioprotection/image2.jpeg" alt="rayons_et_ecran" />
				<figcaption>Atténuation des différents types de rayons<figcaption/>
			</figure>
			<p class="radioprotection">Ces écrans sont choisis en fonction des 
			caractéristiques des rayonnements ionisants émis. C'est très efficace
			en ce qui concerne les rayonnements alpha et bêta qui peuvent facilement
			être arrêtés complètement. Les rayonnements gamma, en revanche, ne sont
			jamais arrêtés en totalité. Il faut placer un écran de plomb épais ou de 
			béton de largeur considérable pour en arrêter la plus grande partie. Mais
			il en reste toujours un peu au-delà de l'écran. C'est pourquoi, en irradiation 
			externe, les rayons gamma sont les plus dangereux.</p>
			<h3 class="radioprotection">1) Stopper les rayons alpha et beta</h3>
			<p class="radioprotection">Le rayonnement alpha, émis par un atome
			radioactif, est un faisceau de noyaux d’hélium composé de deux protons et deux neutrons.<br/>
			Lourds et chargés électriquement, les noyaux d’hélium sont arrêtés facilement et 
			rapidement par les champs électromagnétiques et les atomes composant la matière.<br/>
			Le rayonnement alpha peut être arrêté par une simple feuille de papier.<br/>
			Le rayonnement béta, émis par un atome radioactif, est un faisceau d'électrons.
			Le rayonnement bêta cause plus de dégâts que le rayonnement alpha car il est chargé 
			électriquement.<br/>
			Le rayonnement bêta doit être arrêté par des écrans dont les atomes qui le 
			constituent ont un faible numéro atomique afin de ne pas favoriser l'émission 
			de rayonnement de freinage.</p>
			<p class="radioprotection">Pour se protéger du rayonnement béta, une simple 
			feuille d’aluminium de quelques millimètres suffit. On peut également utiliser
			une feuille de verre, un écran d'un centimètre de plexiglas ou de laiton, ou
			encore quelques mètres d’air permettent d’arrêter une majorité des particules bêta.</p>
			<h3 class="radioprotection">2) Le plomb attenu le rayonnement gamma</h3>
			<p class="radioprotection">Des trois types de radioactivité, la radioactivité
			gamma est la plus pénétrante et celle dont il est le plus difficile de se protéger.
			On ne peut en théorie l'arrêter complètement mais seulement l'atténuer. 
			Dans la pratique une très forte atténuation équivaut à un arrêt, d'où l'importance
			de trouver de bons écrans.<br/>
			Le photon gamma est absorbé par le milieu par 3 phénomènes principaux que 
			sont l’effet photoélectrique, l’effet Compton et l’effet de création de paires.<br/>
			Avec l’effet photoélectrique, le photon gamma est complètement absorbé par un atome.<br/>
			Avec l’effet Compton, le photon continue sa trajectoire avec une énergie moindre.<br/>
			Avec l’effet de création de paires, le photon disparaît.<br/>
			Ces 3 effets dépendent de l’énergie du photon et du milieu.<br/>
			Pour être efficace dans l’atténuation, il faut donc un effet photoélectrique
			ou création de paires. Or, c’est le Plomb (le matériau à un numéro atomique élevé
			, égal à 82) qui est le plus efficace.<br/>
			Le plomb est un métal noir grisâtre de forte densité. Utilisé dès l’antiquité, 
			il est à l’origine de nombreuses inventions comme le fil à plomb ou encore 
			les caractères d’imprimeries.<br/>
			C’est un matériau toxique qui peut provoquer plusieurs symptômes plus 
			ou moins grave en fonction de la dose inhalée ou absorbée comme des troubles
			digestifs, des encéphalopathies, une cyanose (destruction brutale des globules
			rouges : peau bleue), une insuffisance rénale  etc.<br/>
			De manière un peu plus scientifique en restant relativement simple:<br/>
			L’atténuation du rayonnement gamma (par le plomb ou tout autre matériau) 
			est liée à trois processus jouant un rôle majeur (l’effet photoélectrique,
			l’effet Compton et l’effet de création de paires). Tous ces processus 
			entraînent le transfert de l’énergie partielle ou totale du photon incident, 
			à un électron.</p>
		<figure>
			<img src="images/radioprotection/image3.jpg" alt="effet_photoelectrique"/>
			<figcaption>Effet photoélectrique</figcaption>
		</figure>
		<p class="radioprotection">Effet photoélectrique : l’onde électromagnétique ou photon 
		gamma est entièrement absorbé(e) par l’atome et éjecte un électron du cortège 
		électronique (en générale proche du noyau ou de la couche K). L’énergie de 
		l’électron est identique à l’énergie du photon incident moins l’énergie de liaison de l’atome.<br/>
		L’atome résultant est déficitaire d’un électron, qui sera récupéré dans le milieu 
		et donnera naissance à l’émission d’un rayonnement X caractéristique du matériau
		(réarrangement électronique) ou parfois par un électron Auger (cas plus rares).
		<figure>
			<img src="images/radioprotection/image4.jpg" alt="effet_compton"/>
			<figcaption>Effet Compton</figcaption>
		</figure>
		<p class="radioprotection">Effet Compton : c’est l’effet en général dominant 
		à l’énergie des isotopes courants. Le photon est dévié de sa trajectoire par 
		les électrons libres, et l’énergie du photon est diminuée d’autant (il poursuit
		sa course), le reste de l’énergie est transféré à l’électron.</p>
		<figure>
			<img src="images/radioprotection/image5.jpg" alt="phénomène_absorbtion_rayons_gamma"/>
			<figcaption>Les trois phénomènes d'absorbtion des rayons gamma</figcaption>
		</figure>
		<p class="radioprotection">Production de paires : à partir d’une certaine énergie du
		photon (1.02 MeV),  l’interaction du photon a lieu dans le champ coulombien du noyau,
		il disparaît et donne naissance à une paire électron-positrons dont l’énergie est 
		identique à celle du photon incident (moins l’énergie de création de la paire de 2x0.511 MeV)<br/>
		Ces 3 effets dépendent du numéro atomique Z du matériau et de l’énergie du photon 
		incident. Pour un Z élevé (plomb) et des énergies basses, l’effet photoélectrique est 
		largement prédominant. À énergie intermédiaire, l’effet Compton est prédominant et 
		enfin aux hautes énergies, c’est l’effet de création de Paire qui prédomine.</p>
		<figure>
			<img src="images/radioprotection/image6.jpg" alt="atténuation_rayons"/>
			<figcaption>Atténuation selon le type de rayonnements</figcaption>
		</figure>
		<p class="radioprotection">Le blindage doit être très épais. 
		Par exemple, pour réduire de seulement 30 % ce rayonnement, il 
		est nécessaire d’être derrière 6 cm de plomb, 30 cm de béton ou 54 cm de terre.</p>
		<h2 class="radioprotection">B) Se protéger facilement grâce à des protections individuelles  </h2>
		<p class="radioprotection">Les tenues adéquates regroupent tous les moyens 
		qui protègent contre le dépôt des éléments radioactifs sur la peau et contre 
		leur inhalation : elles dépendent du niveau de risque avéré de contamination, 
		allant simplement de l'emploi de blouses, sur-bottes et gants à usage unique, 
		à celui de tenues de protection vestimentaire complète filtrantes ou jetables 
		et de masques de protection respiratoire (demi-faciaux jetables ou faciaux à
		pouvoir filtrant) .<br/>
		Les gants arrêtent les rayons alphas. Les vêtements plombés 
		(tabliers, protège-thyroïdes, lunettes, gants) sont conçus pour 
		se protéger d’un rayonnement faible. Ils ne sont pas conçus pour se 
		protéger du rayonnement primaire. De nos jour pour les examens à 
		faible kilovoltage, ils sont très efficaces (chirurgie orthopédique,
		chirurgie extrémités ou urologie). Pour les examens à plus haut 
		kilovoltage (aux environs de 100 kV ou plus), les opérateurs doivent 
		également apprendre à utiliser tous les autres moyens de radioprotection
		à leur disposition (Temps et Distance). </p>
